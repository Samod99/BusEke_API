const express = require('express');
const authController = require('../controllers/authController');
const router = express.Router();
const validate = require('../middleware/validationMiddleware');
const { loginSchema } = require('../validators/authValidator');

/**
 * @swagger
 * tags:
 *   name: Authentication
 *   description: Auth management endpoints
 */

/**
 * @swagger
 * /auth/login:
 *   post:
 *     summary: Login a user and retrieve a JWT token
 *     tags: [Authentication]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               username:
 *                 type: string
 *               password:
 *                 type: string
 *     responses:
 *       200:
 *         description: Successful login, returns a JWT token and user details
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 token:
 *                   type: string
 *                   description: JWT token for authenticated requests
 *                 user:
 *                   type: object
 *                   properties:
 *                     username:
 *                       type: string
 *                     email:
 *                       type: string
 *                     role:
 *                       type: string
 *                       enum: [admin, operator, commuter]
 *       401:
 *         description: Invalid credentials
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 error:
 *                   type: string
 *                   example: Invalid credentials
 *       500:
 *         description: Internal server error
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 error:
 *                   type: string
 */
router.post('/login', validate(loginSchema), authController.loginUser);

module.exports = router;
